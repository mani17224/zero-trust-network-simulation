[Interface]
PrivateKey = ${sn_priv}
Address = 10.0.0.2/24
ListenPort = 51820
DNS = 10.0.0.1

PostUp   = iptables -A INPUT -i wg0 -j ACCEPT; iptables -A INPUT -p tcp --dport 8001 -i eth0 -j DROP; iptables -A INPUT -p tcp --dport 8002 -i eth0 -j DROP; iptables -A INPUT -p tcp --dport 8003 -i eth0 -j DROP; iptables -A INPUT -p udp --dport 51820 -j ACCEPT
PostDown = iptables -D INPUT -i wg0 -j ACCEPT; iptables -D INPUT -p tcp --dport 8001 -i eth0 -j DROP; iptables -D INPUT -p tcp --dport 8002 -i eth0 -j DROP; iptables -D INPUT -p tcp --dport 8003 -i eth0 -j DROP; iptables -D INPUT -p udp --dport 51820 -j ACCEPT

[Peer]
# Gateway Node
PublicKey = ${gw_pub}
AllowedIPs = 10.0.0.0/24
