DNS = 10.0.0.1

PostUp   = iptables -A INPUT -i wg0 -j ACCEPT; iptables -A INPUT -p udp --dport 51820 -j ACCEPT
PostDown = iptables -D INPUT -i wg0 -j ACCEPT; iptables -D INPUT -p udp --dport 51820 -j ACCEPT

