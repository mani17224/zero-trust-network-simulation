# ─── Interface Activation ──────────────────────────────────────────────────────
activate_interface() {
    local node="$1"
    local conf_dir="${SCRIPT_DIR}/${node}"

    log_step "Activating WireGuard Interface on ${node}"

    # Detect which node we're running on
    if ! ip link show wg0 &>/dev/null; then
        log_info "Bringing up wg0..."
        wg-quick up "${conf_dir}/wg0.conf"
        log_success "wg0 interface is UP"
    else
        log_warn "wg0 already exists — syncing config (idempotent)"
        wg syncconf wg0 <(wg-quick strip "${conf_dir}/wg0.conf")
        log_success "wg0 config synced"
    fi

    # Enable IP forwarding (required on gateway)
    if [[ "$node" == "gateway" ]]; then
        sysctl -w net.ipv4.ip_forward=1 > /dev/null
        echo "net.ipv4.ip_forward = 1" > /etc/sysctl.d/99-wireguard.conf
        log_success "IP forwarding enabled"
    fi
}

# ─── Print Summary ─────────────────────────────────────────────────────────────
print_summary() {
    log_step "Setup Complete"
    echo -e "${BOLD}Node Public Keys:${NC}"
    echo -e "  ${CYAN}Gateway:${NC}      $(read_key gateway public)"
    echo -e "  ${CYAN}Service Node:${NC} $(read_key service-node public)"
    echo -e "  ${CYAN}Client:${NC}       $(read_key client public)"
    echo ""
    echo -e "${GREEN}${BOLD}✓ WireGuard setup complete!${NC}"
    echo -e "  Run ${YELLOW}./check_tunnel.sh${NC} to verify connectivity."
}

# ─── Main ──────────────────────────────────────────────────────────────────────
main() {
    echo -e "${BOLD}${BLUE}"
    echo "╔══════════════════════════════════════════╗"
    echo "║   Zero Trust — WireGuard VPN Setup       ║"
    echo "╚══════════════════════════════════════════╝"
    echo -e "${NC}"

    check_prerequisites
    generate_all_keys
    configure_gateway
    configure_service_node
    configure_client

    # Activate only if --activate flag is passed
    if [[ "${1:-}" == "--activate" ]]; then
        local node="${2:-gateway}"
        activate_interface "$node"
    else
        log_info "Configs generated. Run with --activate <node> to bring up interface."
    fi

    print_summary
}

main "$@"
