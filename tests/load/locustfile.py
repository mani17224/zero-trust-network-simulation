class DenialStormTasks(TaskSet):
    """Simulate a burst of unauthorized requests to measure OPA denial throughput."""

    @task(1)
    def unauthorized_admin_access(self):
        """Try to access service-c as non-admin — should always be 403."""
        cn = random.choice(["gateway.zerotrust.local", "service-a.zerotrust.local"])
        with self.client.get(
            "/users",
            headers={"X-Client-Cert-CN": cn},
            name="/users [GET - unauthorized]",
            catch_response=True,
        ) as resp:
            if resp.status_code == 403:
                resp.success()
            else:
                resp.failure(f"Expected 403 but got {resp.status_code}")

    @task(1)
    def no_cert_request(self):
        """Request with no CN — should always be 401."""
        with self.client.get(
            "/records",
            name="/records [GET - no cert]",
            catch_response=True,
        ) as resp:
            if resp.status_code == 401:
                resp.success()
            else:
                resp.failure(f"Expected 401 but got {resp.status_code}")


# ── User Classes ───────────────────────────────────────────────────────────────

class NormalUser(HttpUser):
    """
    Simulates a normal authenticated service making typical requests.
    Represents most of the traffic (weight=7).
    """
    weight = 7
    wait_time = between(0.1, 0.5)

    tasks = {
        DataServiceTasks: 6,
        AuthServiceTasks: 2,
        HealthMetricsTasks: 2,
    }


class MonitoringUser(HttpUser):
    """
    Simulates a monitoring agent checking health and metrics frequently.
    """
    weight = 2
    wait_time = between(1.0, 2.0)
    tasks = [HealthMetricsTasks]


class AttackerUser(HttpUser):
    """
    Simulates an attacker making unauthorized requests.
    Used to validate deny rate and OPA performance under denial load.
    """
    weight = 1
    wait_time = between(0.05, 0.2)
    tasks = [DenialStormTasks]
